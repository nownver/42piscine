char	*ft_strcapitalize(char *str)
{
	int	n;

	if (str[0] >= 97 && str[0] <= 122)
	{
		str[0] -= 32;
	}
	n = 1;
	while (str[n] != '\0')
	{
		if (!((str[n - 1] >= 65 && str[n - 1] <= 90)
				|| (str[n - 1] >= 97 && str[n - 1] <= 122)
				|| (str[n - 1] >= 48 && str[n - 1] <= 57)))
		{
			if (str[n] >= 97 && str[n] <= 122)
				str[n] -= 32;
		}
		else
		{
			if (str[n] >= 65 && str[n] <= 90)
				str[n] += 32;
		}
		n++;
	}
	return (str);
}
