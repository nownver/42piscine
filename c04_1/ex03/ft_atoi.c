int	ft_atoi(char *str)
{
	int	sign;
	int	temp;

	sign = 1;
	temp = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		temp *= 10;
		temp += (*str - 48);
		str++;
	}
	return (temp * sign);
}
